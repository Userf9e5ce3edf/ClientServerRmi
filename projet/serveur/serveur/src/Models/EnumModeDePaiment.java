package Models;

/**
 * Cette énumération représente les différents modes de paiement possibles.
 */
public enum EnumModeDePaiment {
    /**
     * Paiement par carte bancaire.
     */
    CARTEBANCAIRE,

    /**
     * Paiement en espèces.
     */
    ESPECE,

    /**
     * Mode de paiement non défini.
     */
    NON_DEFINI,

    /**
     * Paiement par virement.
     */
    VIREMENT
}
